marks = {  # DICTIONARY: Stores key-value pairs
    "shubham": 98,
    "Pankaj": 98,
}
print(marks, type(marks))  # Prints the dictionary and its type

marks2 = (1, 2, 3, "Shubham")  # TUPLE: Immutable sequence of values
print(marks2, type(marks2))  # Prints the tuple and its type

marks3 = [1, 23, 4, "Shubham", "Pankaj"]  # LIST: Ordered, mutable collection
print(marks3, type(marks3))  # Prints the list and its type

print(marks["shubham"])  # Accesses the value for key 'shubham' in the dictionary
